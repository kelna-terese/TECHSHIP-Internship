function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log("Addition:", add(50, 100));
console.log("Subtraction:", subtract(12, 6));
console.log("Multiplication:", multiply(0, 3));
console.log("Division:", divide(2, 4));